When is the sampling frequency exact?
The sampling frequency is often only approximate and the interval
 between observations is not quite a fixed unit.
For example, there are usually 365 days in a year based on the Gregorian calendar.
However, (almost) every four years there are 366 days (leap years).
 This compensates for the fact that the Earth completes a rotation around Sol,
 the sun, in approximately 365.2422 days, on average.
As a simplifying assumption, we often ignore
these small discrepancies and proceed as though the sampling frequency
and observation intervals are fixed constants.
Typically, our results will not be sensitive to approximation
when the underlying process is not changing too quickly.
