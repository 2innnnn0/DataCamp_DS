# 해석
샘플링 빈도는 정확히 언제입니까?
샘플링 빈도는 종종 단지 근사치이며 관측 간격은 고정 된 단위가 아닙니다.
예를 들어, 그레고리력에 기초하여 일년 중 보통 365 일이 있습니다.
그러나 (거의) 4 년마다 366 일 (윤년)이 있습니다.
이것은 지구가 평균 약 365.2422 일 동안 태양 (태양) 주위의 회전을 완료한다는 사실을 보완합니다.
단순화 가정으로서, 우리는 종종 이러한 작은 불일치를 무시하고
샘플링 빈도 및 관찰 간격이 고정 상수 인 것처럼 진행합니다.
일반적으로 기본 프로세스가 너무 빨리 변경되지 않으면 우리의 결과는 근사치에 민감하지 않습니다.

정확한 샘플링 빈도는 다음 중 어느 것입니까?

1.수년 동안 매월 마지막 날에 기록 된 월간 관찰
2.매주 금요일 수년간 주간 관찰 기록
3.일일 관측치는 수 주간 "업무 일"마다 기록됩니다.
4.며칠 동안 매 시간마다 기록 된 시간별 관찰
